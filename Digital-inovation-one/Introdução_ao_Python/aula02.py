# variaveis no python e operações

#entrada de dados pelo usuário

a = int(input('Insira o primeira valor : '))
b = int(input('Insira o segundo valor: '))

#operações 
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
potencia = a ** b
resto = a % b

print(f'''
soma: {soma}
subtração: {subtracao}
multiplicação: {multiplicacao}
divisão: {divisao}
potência: {potencia}
resto: {resto}
''')



