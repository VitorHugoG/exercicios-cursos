#Operações no Python

#variáveis
a = 5
b = 3

#Adição
print("Adição: ", a + b )

#Subtração
print("Subtração: ", a - b )

#Multiplicação
print("Multiplicação: ", a * b )

#Divisão fracionária
print("Divisão Fracionária: ", a / b )

#Divisão Inteira
print("Divisão Inteira: ", a // b)

#Resto
print("Resto da Divisão: ", a % b )




